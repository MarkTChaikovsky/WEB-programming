/* Задача 20. Створити функцію, яка для 4 чисел знаходять середнє арифметичне; (Стрілочна функція) */
let answear = (m1,m2,m3,m4) => alert(`середнє арифметичне: ${(m1+m2+m3+m4)/4}`);
let n1 = parseInt(prompt("Введыть 1-ше число: "));
let n2 = parseInt(prompt("Введыть 2-ге число: "));
let n3 = parseInt(prompt("Введыть 3-тє число: "));
let n4 = parseInt(prompt("Введыть 4-те число: "));
answear(n1,n2,n3,n4); 